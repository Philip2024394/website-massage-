/**
 * Get meta tags for a specific country
 */
export const getCountryMetaTags = (countryCode: string, language: string = 'en') => {
  const seo = COUNTRY_SEO[countryCode];
  if (!seo) return null;

  const isNativeLanguage = language === seo.language && language !== 'en';
  const title = isNativeLanguage && seo.titleEn ? seo.title : (seo.titleEn || seo.title);
  const description = isNativeLanguage && seo.descriptionEn ? seo.description : (seo.descriptionEn || seo.description);
  const keywords = isNativeLanguage && seo.keywordsEn ? seo.keywords : (seo.keywordsEn || seo.keywords);

  return {
    title,
    description,
    keywords,
    canonical: seo.canonicalUrl,
    ogLocale: seo.ogLocale,
    ogImage: 'https://ik.imagekit.io/7grri5v7d/Massage%20hub%20indastreet.png',
    twitterCard: 'summary_large_image',
    structuredData: generateCountryStructuredData(countryCode)
  };
};
