/**
 * Validate terms acceptance data
 */
export function validateTermsAcceptance(data: {
  termsAccepted: boolean;
  termsAcceptedAt?: string;
  independentContractor?: boolean;
}): { valid: boolean; error?: string } {
  if (!data.termsAccepted) {
    return { valid: false, error: 'Terms must be accepted' };
  }
  
  if (!data.termsAcceptedAt) {
    return { valid: false, error: 'Terms acceptance timestamp required' };
  }
  
  if (data.independentContractor === false) {
    return { valid: false, error: 'Independent contractor status must be accepted' };
  }
  
  return { valid: true };
}

