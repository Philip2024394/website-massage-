async function addJobPostings() {
    console.log('Starting to add job postings to Appwrite...\n');
    
    let successCount = 0;
    let errorCount = 0;
    
    const postedDate = new Date().toISOString();

    for (const job of jobPostings) {
        try {
            const result = await databases.createDocument(
                DATABASE_ID,
                COLLECTION_ID,
                ID.unique(),
                {
                    ...job,
                    postedDate
                }
            );
            console.log(`✅ Added: ${job.jobTitle} at ${job.businessName}`);
            successCount++;
        } catch (error) {
            console.error(`❌ Failed to add: ${job.jobTitle} at ${job.businessName}`);
            console.error('Error:', error);
            errorCount++;
        }
    }

    console.log(`\n📊 Summary:`);
    console.log(`   ✅ Success: ${successCount}`);
    console.log(`   ❌ Errors: ${errorCount}`);
    console.log(`   📝 Total: ${jobPostings.length}`);
}

// Run the script
addJobPostings()
    .then(() => {
        console.log('\n✨ Job postings addition completed!');
        process.exit(0);
    })
    .catch((error) => {
        console.error('\n💥 Script failed:', error);
        process.exit(1);
    });
